#include <stdio.h>

/**
 * add - adds two integers a and b
 * @a: the first input integer
 * @b: the second input integer
 * Return: the result of the addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers, a from b
 * @a: first input integer
 * @b: second input integer
 * Return: result of subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers a and b
 * @a: first input integer
 * @b: second input integer
 * Return: product of the two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers, a by b
 * @a: first input integer
 * @b: the second integer
 * Return: the division result
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - returns the remainder of a division
 * @a: first input integer
 * @b: second input integer
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
